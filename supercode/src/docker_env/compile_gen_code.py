# paths
bench_name = "humaneval"
num_samples_per_task=5 ; iter = "" #"_x3"
model_name = "Qwen3-4B" # "Qwen3-Coder-Next" # "Qwen2.5-Coder-3B-Instruct" # "Qwen2.5-Coder-3B-Instruct+Qwen3-4B" #
