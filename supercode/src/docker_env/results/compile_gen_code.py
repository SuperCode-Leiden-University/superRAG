verbose = 0
def check_unit_tests(function, test=None, entry_point=None):
    if verbose>0 : print(">> using the check_unit_tests")
    # this checks if the unit tests return the expected result (functional correctness)

    if test == None:
        #print(">> compilation check")
        with tempfile.NamedTemporaryFile("w", suffix=".py", delete=False) as tmp:
            tmp.write(function)
            tmp_path = tmp.name
        try:
            result = subprocess.run(  # run a program from inside the container (and remove the container afterwards)
                ["python3", tmp_path],
                capture_output=True,
                text=True,
                timeout=10  # seconds
            )
            if verbose>1 : print(">> command run successfully\n", result)
            return result.returncode, "stdout=" + result.stdout + "\n" + "stderr=" + result.stderr
        except Exception as e:
            print("Error in check_unit_tests tool:", e)
            return "Error in check_unit_tests tool:"+e

    else:
        #print(">> test check")
        with tempfile.NamedTemporaryFile("w", suffix=".py", delete=False) as tmp:
            tmp.write(function+"\n\n"+test+"\n\ncheck("+entry_point+")")
            tmp_path = tmp.name
        try:
            result = subprocess.run(  # run a program from inside the container (and remove the container afterwards)
                ["python3", tmp_path],
                capture_output=True,
                text=True,
                timeout=10  # seconds
            )
            #print(">> command run successfully\n", result)
            return result.returncode, "stdout=" + result.stdout + "\n" + "stderr=" + result.stderr
        except Exception as e:
            print("Error in check_unit_tests tool:", e)
            return "Error in check_unit_tests tool:"+e


# paths
gen_code_dir = "src/docker_env/results" # where I save the code generated by the agent, so it can be tested
#file_name = "humaneval_patch-"+model_id[model_id.find("/")+1:] # humaneval_baseline or humaneval_patch or humaneval_megalinter
file_name = "humaneval_benchmark-Qwen2.5-Coder-3B-Instruct_5" # humaneval_baseline or humaneval_benchmark ### old: humaneval_patch or humaneval_megalinter
