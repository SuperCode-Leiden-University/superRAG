comp_pass = 0
comp_fail = 0
test_pass = 0
test_fail = 0
samples = []

# importing the benchmark from hugging face
print("loading benchmark...")
dataset = load_dataset("openai/openai_humaneval")
print("reading the problems...")
problems = read_problems()
#problems = {row["task_id"]: row for row in dataset["test"]}

print("reading jsonl for "+file_name+"...")
with open(gen_code_dir+"/"+file_name+".jsonl") as f:
    for i, line in enumerate(f, start=1):
        try:
            samples.append(ast.literal_eval(line))
        except Exception as e:
            print("Error on line", i)
            print("Line content:", repr(line))
            raise

print("evaluating the code...")
for sample in samples:
    print("\n------------------------------------------------------\n### sample:", sample["task_id"])
    task_id = sample["task_id"]
    function = sample["code"]
    entry_point = problems[task_id]["entry_point"] # name of the function
    test = problems[task_id]["test"]

    # check just the code
    returncode, output = check_unit_tests(function)
    print("compiler: returncode =", returncode, "\n\n", output)
    if "no errors" in output: comp_pass+=1
    else: comp_fail+=1

    # check with test cases
    returncode, output = check_unit_tests(function, test, entry_point)
    print("test: returncode =", returncode, "\n\n", output)
    if "no errors" in output: test_pass+=1
    else: test_fail+=1

# Final summary
print("\n==============================")
print("Execution Summary for "+file_name)
print("==============================")
print(f"Total samples:\t{len(samples)}")
print(f"compiler pass:\t{comp_pass}\t({comp_pass/len(samples)*100:.2f}%)")
print(f"compiler fail:\t{comp_fail}\t({comp_fail/len(samples)*100:.2f}%)")
print(f"test pass:   \t{test_pass}\t({test_pass/len(samples)*100:.2f}%)")
print(f"test fail:   \t{test_fail}\t({test_fail/len(samples)*100:.2f}%)")

