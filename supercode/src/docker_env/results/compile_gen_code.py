print("start...")
import subprocess
import ast
import tempfile

from human_eval.data import write_jsonl, read_problems
from datasets import load_dataset # load datasets from Hugging Face
from src.tools.tools import check_unit_tests
#from src.configs.parse_config import model_id, verbose, gen_code_dir


# paths
gen_code_dir = "src/docker_env/results" # where I save the code generated by the agent, so it can be tested
#file_name = "humaneval_patch-"+model_id[model_id.find("/")+1:] # humaneval_baseline or humaneval_patch or humaneval_megalinter
file_name = "humaneval_patch-Qwen2.5-Coder-3B-Instruct-1+2" # humaneval_baseline or humaneval_patch or humaneval_megalinter
#benchmark_file = gen_code_dir+"/"+file_name+"_results.txt"

comp_pass = 0
comp_fail = 0
test_pass = 0
test_fail = 0
samples = []

# importing the benchmark from hugging face
print("loading benchmark...")
dataset = load_dataset("openai/openai_humaneval")
print("reading the problems...")
problems = read_problems()
#problems = {row["task_id"]: row for row in dataset["test"]}

print("reading jsonl for "+file_name+"...")
with open(gen_code_dir+"/"+file_name+".jsonl") as f:
    for i, line in enumerate(f, start=1):
        try:
            samples.append(ast.literal_eval(line))
        except Exception as e:
            print("Error on line", i)
            print("Line content:", repr(line))
            raise

print("evaluating the code...")
for sample in samples:
    #print("### sample:", sample["task_id"])
    task_id = sample["task_id"]
    function = sample["code"]
    entry_point = problems[task_id]["entry_point"] # name of the function
    test = problems[task_id]["test"]

    # check just the code
    if check_unit_tests(function)[0]==0: comp_pass+=1
    else: comp_fail+=1

    # check with test cases
    if check_unit_tests(function, test, entry_point)[0]: test_pass+=1
    else: test_fail+=1

# Final summary
print("\n==============================")
print("Execution Summary for "+file_name)
print("==============================")
print(f"Total samples: {len(samples)}")
print(f"compiler pass: {comp_pass} ({comp_pass/len(samples)*100:.2f}%)")
print(f"compiler fail: {comp_fail} ({comp_fail/len(samples)*100:.2f}%)")
print(f"test pass: {test_pass} ({test_pass/len(samples)*100:.2f}%)")
print(f"test fail: {test_fail} ({test_fail/len(samples)*100:.2f}%)")

